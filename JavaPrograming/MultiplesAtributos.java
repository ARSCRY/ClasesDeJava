public class MultiplesAtributos {
    String nombre = "Dana";
    String apellido = "Ramirez";
    int edad = 18;

    public static void main(String[] args) {
        MultiplesAtributos misDatos = new MultiplesAtributos();

        System.out.println("Nombre: "+misDatos.nombre);
        System.out.println("Apellido: "+misDatos.apellido);
        System.out.println("Edad: "+misDatos.edad);
    }
    
}
