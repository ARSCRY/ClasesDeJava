public class MathProblemas {
    public static void main(String[] args) {
        short numeroRandom = (short)(Math.random()*101);
        System.out.println("Numero \"Maximo\": "+Math.max(5,10));
        System.out.println("Numero \"Minimo\": "+Math.min(4, 6));
        System.out.println("Raiz cuadrada: "+Math.sqrt(64));
        System.out.println("Valor absoluto: "+Math.abs(-4.5));
        System.out.println("Numero aleatorio: "+Math.random());
        System.out.println("Numero aleatorio nuevo: "+numeroRandom);

    }
    
}
